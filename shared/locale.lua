function Shared.L(key, ...)
    local tbl = Shared.Locale[Config.Locale] or Shared.Locale.en
    local str = tbl[key] or Shared.Locale.en[key] or key
    if select('#', ...) > 0 then return string.format(str, ...) end
    return str
end
